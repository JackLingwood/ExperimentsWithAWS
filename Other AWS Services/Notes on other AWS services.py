# ELASTIC BEANSTALK
# Elastic Beanstalk is a fully managed service that streamlines the deployment, management, and scaling of web applications. Developers can upload their code,
# and Elastic Beanstalk automatically handles the provisioning of infrastructure, scaling, load balancing, and application health monitoring. It supports various
# programming languages and frameworks, such as Java, .NET, Python, Node.js, Docker, and more. It provides full control over the underlying AWS resources while
# automating many operational tasks.

# AWS BATCH
# AWS Batch is a fully managed service that enables developers, scientists, and engineers to easily and efficiently run batch computing workloads on the AWS Cloud.
# It dynamically provisions the optimal quantity and type of compute resources (e.g., CPU or memory optimized instances) based on the volume and specific resource
# requirements of the batch jobs submitted.

# AWS CloudFormation is a service that helps you model and set up your Amazon Web Services resources so that you can spend less time managing those resources and
# more time focusing on your applications that run in AWS. You create a template that describes all the AWS resources that you want (like Amazon EC2 instances or
# Amazon RDS DB instances), and AWS CloudFormation uses that template to provision those resources for you.

# AWS LIGHTSAIL
# Amazon Lightsail is a simplified cloud platform that offers everything needed to build an application or website, plus a cost-effective, monthly plan.
# It provides developers with compute, storage, and networking capabilities, along with a simplified management console
# and API. Lightsail is designed to be easy to use, making it a great choice for developers who want to get started quickly without needing to understand the
# complexities of AWS.


# AWS Outposts
# AWS Outposts is a fully managed service that extends AWS infrastructure, services, APIs, and tools to virtually any customer site for a truly consistent hybrid
# cloud experience.
# It allows you to run AWS services on-premises using the same hardware and software as in AWS data centers, enabling low-latency applications and local data processing.

# AWS Snow Family
